func randString(n int) string {
	letterRunes := []rune("abcdefghijklmnopqrstuvwxyz")
	b := make([]rune, n)
	for i := range b {
		b[i] = letterRunes[rand.Intn(len(letterRunes))]
	}
	return string(b)
}

func rbyte(n int) {
	r := rand.New(rand.NewSource(time.Now().UnixNano()))
	s := r.Perm(n)

	fmt.Println(len(s))
	mod := 0
	for _, iem := range s {
		fmt.Printf("%0#2x, ", iem)
		mod++
		if mod%8 == 0 {
			fmt.Printf("\n")
		}
	}
}

